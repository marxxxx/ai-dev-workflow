/**
 * Every MCP server below is added scoped to *this project only* — never to a harness's
 * global/user-wide config — so machines with multiple projects don't leak servers (or API
 * keys) across them.
 */
function superpowersSection() {
  return [
    '## superpowers',
    '',
    'Skill library that drives the brainstorm → plan → implement workflow. Available on Claude',
    'Code, Codex, and OpenCode (https://github.com/obra/superpowers).',
    '',
    '**Claude Code** — official plugin marketplace:',
    '',
    '```',
    '/plugin install superpowers@claude-plugins-official',
    '```',
    '',
    '**Codex** — Codex only discovers skills from the global `~/.agents/skills/` directory, so',
    'unlike the MCP servers above this cannot be scoped to one project. Official install',
    '(https://obra-superpowers.mintlify.app/installation/codex):',
    '',
    '```bash',
    'git clone https://github.com/obra/superpowers.git ~/.codex/superpowers',
    'mkdir -p ~/.agents/skills',
    'ln -s ~/.codex/superpowers/skills ~/.agents/skills/superpowers',
    '```',
    '',
    '**OpenCode** — OpenCode *does* support a project-local plugin dir (`.opencode/plugins/`) and',
    'project-local skills (`.opencode/skills/`), so the same clone-and-symlink approach from the',
    'official guide (https://obra-superpowers.mintlify.app/installation/opencode) can be pointed at',
    'the project instead of `~/.config/opencode/`:',
    '',
    '```bash',
    'ROOT="$(pwd)"',
    'git clone https://github.com/obra/superpowers.git "$ROOT/.opencode/vendor/superpowers"',
    'mkdir -p "$ROOT/.opencode/plugins" "$ROOT/.opencode/skills"',
    'ln -sf "$ROOT/.opencode/vendor/superpowers/.opencode/plugins/superpowers.js" "$ROOT/.opencode/plugins/superpowers.js"',
    'ln -sf "$ROOT/.opencode/vendor/superpowers/skills" "$ROOT/.opencode/skills/superpowers"',
    '```',
    '',
    'Add `.opencode/vendor/` to `.gitignore` (it\'s a vendored clone, not source) and restart',
    'OpenCode. Update later with `git -C .opencode/vendor/superpowers pull`.',
    '',
  ];
}

function context7Section() {
  return [
    '## context7',
    '',
    'MCP server that fetches up-to-date library/framework documentation on demand. Free to use',
    'anonymously; sign up at https://context7.com for an API key to raise the rate limit (optional).',
    '',
    '**Claude Code** — `--scope local` keeps it private to this project + machine (not written to',
    'the shared `.mcp.json`, not available to other projects):',
    '',
    '```bash',
    'claude mcp add --scope local --transport http context7 https://mcp.context7.com/mcp \\',
    '  --header "CONTEXT7_API_KEY: YOUR_API_KEY"',
    '```',
    '',
    '**Codex** — `codex mcp add` only writes to the global `~/.codex/config.toml`, so add it to a',
    'project-local `.codex/config.toml` instead (`env_http_headers` reads the key from your shell',
    "environment at runtime, so the key itself never lands in the committed file). Codex disables",
    'project-local config the first time it sees a new repo — accept the trust prompt on first run:',
    '',
    '```bash',
    'mkdir -p .codex',
    'cat >> .codex/config.toml <<' + "'EOF'",
    '',
    '[mcp_servers.context7]',
    'url = "https://mcp.context7.com/mcp"',
    '',
    '[mcp_servers.context7.env_http_headers]',
    'CONTEXT7_API_KEY = "CONTEXT7_API_KEY"',
    'EOF',
    '```',
    '',
    '**OpenCode** — add to (or create) `opencode.json` at the project root; it\'s safe to commit',
    'since the key is referenced via `{env:...}`, not inlined:',
    '',
    '```jsonc',
    '{',
    '  "$schema": "https://opencode.ai/config.json",',
    '  "mcp": {',
    '    "context7": {',
    '      "type": "remote",',
    '      "url": "https://mcp.context7.com/mcp",',
    '      "headers": { "CONTEXT7_API_KEY": "{env:CONTEXT7_API_KEY}" }',
    '    }',
    '  }',
    '}',
    '```',
    '',
  ];
}

