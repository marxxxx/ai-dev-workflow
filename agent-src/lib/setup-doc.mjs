/** Render docs/ai-workflow-setup.md: recommended plugins + per-harness MCP server setup. */
export function renderSetupDoc(config) {
  const azure = config.ticketing?.backend === 'azure-devops';
  const e2e = !!config.e2e;
  const lines = [
    '<!-- DO NOT EDIT — generated by `ai-dev-workflow init`; re-run init to refresh. -->',
    '',
    `# AI Workflow Setup — ${config.project?.name || 'your project'}`,
    '',
    'This workflow supports **Claude Code**, **Codex**, and **OpenCode**, plus one recommended',
    'skill library and three recommended MCP servers. Install what your harness(es) need — every',
    'MCP server (and, where supported, the skill library) is scoped to this project only, never',
    'installed globally.',
    '',
    ...superpowersSection(),
    ...context7Section(),
    ...serenaSection(),
    ...playwrightSection(),
  ];
  if (azure) {
    lines.push(
      '## Azure DevOps `ado` MCP server',
      '',
      'For the `azure-devops` ticketing backend, `ai-dev-workflow generate` automatically merges an',
      '`ado` server entry into `.mcp.json` (the `@azure-devops/mcp` server handles its own auth).',
      'Nothing to install by hand — just run `generate` and reload your MCP servers.',
      '',
    );
  }
  if (e2e) {
    lines.push(
      '## End-to-end environment',
      '',
      'The QA engineer starts the app for browser testing through a fixed contract. Two',
      'project-owned commands implement it (configured in `ai-project.json` under `e2e`):',
      '',
      `- **up** (\`${config.e2e.up}\`) — start backing services + the app, block until reachable,`,
      `  print \`BASE_URL=<url>\` to stdout, log under \`${config.e2e.logsDir}\`, and exit 0 within`,
      `  ${config.e2e.readinessTimeout}s (non-zero on failure/timeout).`,
      `- **down** (\`${config.e2e.down}\`) — idempotent teardown of everything \`up\` started.`,
      '',
      'Stub scripts were scaffolded — fill them in for your app (start any backing services,',
      'apply migrations/seed, start the app, block until reachable, and echo `BASE_URL=<url>`).',
      'Until implemented they exit 0 without a `BASE_URL`, so QA skips browser e2e and defers UI',
      'criteria to human review rather than failing. A non-zero `up` (a real startup failure) is a',
      'blocker.',
      '',
    );
  }
  lines.push(
    '## Next steps',
    '',
    '1. `npx github:marxxxx/ai-dev-workflow generate` — render the platform files.',
    '2. Commit `ai-project.json` and the generated `.claude/`, `.codex/`, `.opencode/`, `.agents/` dirs.',
    '',
  );
  return lines.join('\n');
}
